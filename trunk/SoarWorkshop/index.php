		<a id="Overview"></a>
		<h2>Overview</h2>

		<p>
			Each year members of the Soar community--faculty, scientists, graduate 
			students, technical staff and developers--gather together for several days of 
			intensive interaction and exchange on Soar. The Soar community is widely 
			distributed geographically. These workshops provide the opportunity for 
			face-to-face conversations, hearing about the current status of other 
			participants' research, and looking forward to what will happen in the future. 
			We will try to give as many members of the community as possible the 
			opportunity to describe their research or discuss the Soar issues that are of 
			concern to them. This means the time available per talk is quite short 
			(typically either 5 or 15 minutes). Since workshop attendees are already 
			entrenched in the Soar world, brief talks that concentrate on only the 
			essentials work very well.
		</p>
		<p>
			The Soar Workshop is announced on the <a href="http://lists.sourceforge.net/mailman/listinfo/soar-group">soar-group</a>
			mailing list, but we want 
			to ensure that everyone actively working with Soar is invited. If you know of 
			anyone who should have been invited but did not receive an announcement, please 
			let us know. Proceedings from the previous Soar workshops are available online:</p>
